// ==================== STATE ====================
const state = {
  toolsGenerated: 0,
  tools: new Map(), // toolId -> tool config
  totalRevenue: 0,
  startTime: Date.now()
};

// ==================== UTILITIES ====================
function generateToolId(type, template) {
  return `${type}-${template}-${uuidv4().slice(0, 8)}`;
}

function createToolDirectory(toolId) {
  const dir = path.join(process.cwd(), 'tools', toolId);
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
  return dir;
}

function generateToolCode(type, template, toolId, customOptions = {}) {
  const toolConfig = TOOL_TYPES[type];
  const dir = createToolDirectory(toolId);
  
  // Create package.json
  const packageJson = {
    name: toolId,
    version: '1.0.0',
    description: toolConfig.description,
    main: 'index.js',
    scripts: {
      start: 'node index.js',
      test: 'node test.js'
    },
    dependencies: {
      express: '^4.18.2',
      'node-fetch': '^3.3.1',
     axios: '^1.6.0',
      chalk: '^4.1.2',
      dotenv: '^16.0.3'
    }
  };
  
  // Add specific dependencies based on template
  if (template.includes('puppeteer')) {
    packageJson.dependencies.puppeteer = '^21.0.0';
  }
  if (template.includes('playwright')) {
    packageJson.dependencies.playwright = '^1.40.0';
  }
  
  fs.writeFileSync(path.join(dir, 'package.json'), JSON.stringify(packageJson, null, 2));
  
  // Create main index.js
  const indexJs = generateMainFile(type, template, toolId, toolConfig);
  fs.writeFileSync(path.join(dir, 'index.js'), indexJs);
  
  // Create README.md
  const readme = `# ${toolConfig.name} (${toolId})\n\n${toolConfig.description}\n\n## Installation\n\n\`\`\`bash\nnpm install\nnpm start\n\`\`\`\n\n## Revenue Model\n\n- Base price: $${toolConfig.basePrice}\n- Revenue per use: $${toolConfig.revenuePerUse}\n\n## API Endpoints\n\n- GET /health - Health check\n- POST /execute - Execute tool\n\n## Configuration\n\nCreate .env file with required environment variables.\n`;
  fs.writeFileSync(path.join(dir, 'README.md'), readme);
  
  // Create .env.example
  const envExample = generateEnvExample(type, template);
  fs.writeFileSync(path.join(dir, '.env.example'), envExample);
  
  // Create test file
  const testFile = `// Test script for ${toolId}\nimport { exec } from 'child_process';\nconsole.log('Testing ${toolId}...');\n`;
  fs.writeFileSync(path.join(dir, 'test.js'), testFile);
  
  return dir;
}

