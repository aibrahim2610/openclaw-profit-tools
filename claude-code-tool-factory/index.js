function generateEnvExample(type, template) {
  return `# Environment configuration for ${type}-${template}
PORT=3000
NODE_ENV=production

# Add your API keys here
# OPENROUTER_API_KEY=sk-or-v1-xxx
# OTHER_API_KEYS=xxx

# Tool-specific configuration
# CONFIG_OPTION=value
`;
}

// ==================== API ROUTES ====================

// Get available tool types
app.get('/api/tool-types', (req, res) => {
  const types = Object.entries(TOOL_TYPES).map(([key, config]) => ({
    id: key,
    ...config
  }));
  res.json(types);
});

// Generate a tool
app.post('/api/generate', async (req, res) => {
  const { type, template, customOptions } = req.body;
  
  if (!TOOL_TYPES[type]) {
    return res.status(400).json({ error: 'Invalid tool type' });
  }
  
  if (!TOOL_TYPES[type].templates.includes(template)) {
    return res.status(400).json({ error: 'Invalid template for this tool type' });
  }
  
  const toolId = generateToolId(type, template);
  const toolDir = createToolDirectory(toolId);
  const toolConfig = TOOL_TYPES[type];
  
  // Generate code files
  generateToolCode(type, template, toolId, customOptions);
  
  // Store tool metadata
  const toolMeta = {
    id: toolId,
    type,
    template,
    name: toolConfig.name,
    description: toolConfig.description,
    basePrice: toolConfig.basePrice,
    revenuePerUse: toolConfig.revenuePerUse,
    createdAt: new Date().toISOString(),
    directory: toolDir,
    status: 'generated'
  };
  
  state.tools.set(toolId, toolMeta);
  state.toolsGenerated++;
  
  // Install dependencies
  try {
    console.log(chalk.yellow(`Installing dependencies for ${toolId}...`));
    // In production, you'd spawn npm install here
    // For now, return success
  } catch (error) {
    console.error(chalk.red(`Failed to install dependencies: ${error.message}`));
  }
  
  res.json({
    success: true,
    tool: toolMeta,
    message: `✅ Tool generated successfully: ${toolId}`
  });
});

// List all generated tools
app.get('/api/tools', (req, res) => {
  const tools = Array.from(state.tools.values());
  res.json({
    total: tools.length,
    tools,
    totalPotentialRevenue: tools.reduce((sum, tool) => sum + tool.revenuePerUse * 1000, 0) // assuming 1000 uses
  });
});

// Get tool details
app.get('/api/tools/:toolId', (req, res) => {
  const tool = state.tools.get(req.params.toolId);
  if (!tool) {
    return res.status(404).json({ error: 'Tool not found' });
  }
  res.json(tool);
});

// Delete a tool
app.delete('/api/tools/:toolId', (req, res) => {
  const tool = state.tools.get(req.params.toolId);
  if (!tool) {
    return res.status(404).json({ error: 'Tool not found' });
  }
  
  // In production, would delete directory
  state.tools.delete(req.params.toolId);
  res.json({ success: true, message: `Tool ${req.params.toolId} deleted` });
});

// Factory statistics
app.get('/admin/stats', (req, res) => {
  const tools = Array.from(state.tools.values());
  const uptime = process.uptime();
  
  res.json({
    toolsGenerated: state.toolsGenerated,
    activeTools: tools.length,
    totalPotentialRevenue: tools.reduce((sum, tool) => sum + tool.revenuePerUse * 1000, 0),
    uptime,
    toolTypes: Object.keys(TOOL_TYPES),
    startTime: state.startTime
  });
});

// ==================== START SERVER ====================
app.listen(PORT, () => {
  console.log(chalk.green(`\n🤖 Claude Code Tool Factory Started`));
  console.log(chalk.blue(`📍 Server: http://localhost:${PORT}`));
  console.log(chalk.yellow(`🛠️  Available tool types: ${Object.keys(TOOL_TYPES).join(', ')}`));
  console.log(chalk.cyan(`📊 Generated tools: ${state.toolsGenerated}`));
  console.log(chalk.white(`\nUse the API to generate tools, or use the interactive CLI:`));
  console.log(chalk.gray(`  node generate-tool.js`));
});
