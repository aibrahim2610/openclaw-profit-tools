console.log(chalk.green('\n🤖 Claude Code Tool Factory - Interactive CLI'));
console.log(chalk.gray('=====================================\n'));

async function main() {
  const { action } = await inquirer.prompt([
    {
      type: 'list',
      name: 'action',
      message: 'What would you like to do?',
      choices: [
        { name: '🚀 Generate a new tool', value: 'generate' },
        { name: '📋 List all tool types', value: 'list' },
        { name: '📁 List generated tools', value: 'tools' },
        { name: '❌ Exit', value: 'exit' }
      ]
    }
  ]);

  if (action === 'exit') {
    console.log(chalk.yellow('Goodbye!'));
    process.exit(0);
  }

  if (action === 'list') {
    console.log(chalk.cyan('\n📋 Available Tool Types'));
    console.log(chalk.gray('========================='));
    
    for (const [type, config] of Object.entries(TOOL_TYPES)) {
      console.log(chalk.bold(`\n${config.name}`));
      console.log(`  Type: ${type}`);
      console.log(`  Description: ${config.description}`);
      console.log(`  Templates: ${config.templates.join(', ')}`);
      console.log(`  Revenue per use: $${config.revenuePerUse}`);
      console.log(`  Base price: $${config.basePrice}`);
    }
    
    console.log('');
    return main();
  }

  if (action === 'generate') {
    const answers = await inquirer.prompt([
      {
        type: 'list',
        name: 'toolType',
        message: 'Choose tool type:',
        choices: Object.keys(TOOL_TYPES),
        pageSize: 10
      },
      {
        type: 'list',
        name: 'template',
        message: 'Choose template:',
        choices: (answers) => TOOL_TYPES[answers.toolType].templates,
        pageSize: 8
      },
      {
        type: 'input',
        name: 'customName',
        message: 'Tool name (optional, press Enter for default):',
        default: (answers) => `${TOOL_TYPES[answers.toolType].name} (${answers.template})`
      },
      {
        type: 'confirm',
        name: 'confirm',
        message: 'Generate this tool?',
        default: true
      }
    ]);

    if (!answers.confirm) {
      console.log(chalk.yellow('Cancelled.'));
      return main();
    }

    const toolId = generateToolId(answers.toolType, answers.template);
    const toolConfig = TOOL_TYPES[answers.toolType];
    
    console.log(chalk.cyan('\n🏗️  Generating tool...'));
    console.log(chalk.gray('Tool ID:'), chalk.bold(toolId));
    console.log(chalk.gray('Name:'), chalk.bold(answers.customName || toolConfig.name));
    console.log(chalk.gray('Type:'), chalk.bold(answers.toolType));
    console.log(chalk.gray('Template:'), chalk.bold(answers.template));
    
    try {
      // Create directory
      const toolDir = createToolDirectory(toolId);
      console.log(chalk.gray('Directory:'), toolDir);
      
      // Generate files
      const packageJson = {
        name: toolId,
        version: '1.0.0',
        description: toolConfig.description,
        main: 'index.js',
        scripts: {
          start: 'node index.js',
          test: 'node test.js'
        },
        dependencies: {
          express: '^4.18.2',
          'node-fetch': '^3.3.1',
          axios: '^1.6.0',
          chalk: '^4.1.2',
          dotenv: '^16.0.3'
        }
      };
      
      if (answers.template.includes('puppeteer')) {
        packageJson.dependencies.puppeteer = '^21.0.0';
      }
      if (answers.template.includes('playwright')) {
        packageJson.dependencies.playwright = '^1.40.0';
      }
      
      fs.writeFileSync(path.join(toolDir, 'package.json'), JSON.stringify(packageJson, null, 2));
      
      // Generate main file
      const mainCode = generateMainFile(answers.toolType, answers.template, toolId, toolConfig);
      fs.writeFileSync(path.join(toolDir, 'index.js'), mainCode);
      
      // Generate README
      const readme = generateReadme(answers.toolType, answers.template, toolId, toolConfig);
      fs.writeFileSync(path.join(toolDir, 'README.md'), readme);
      
      // Generate .env.example
      const envExample = generateEnvExample(answers.toolType, answers.template, toolConfig);
      fs.writeFileSync(path.join(toolDir, '.env.example'), envExample);
      
      // Generate test file
      const testContent = `// Test script for ${toolId}
import { exec } from 'child_process';
import chalk from 'chalk';

console.log(chalk.cyan('Testing ${toolId}...'));

// Add your test logic here

console.log(chalk.green('✅ Test completed'));
`;
      fs.writeFileSync(path.join(toolDir, 'test.js'), testContent);
      
      console.log(chalk.green('\n✅ Tool generated successfully!'));
      console.log(chalk.gray('Files created in:'), toolDir);
      console.log(chalk.yellow('\nNext steps:'));
      console.log(`  1. cd ${toolDir}`);
      console.log('  2. npm install');
      console.log('  3. npm start');
      console.log('  4. Edit .env with your API keys');
      
      console.log(chalk.green('\n💰 Revenue potential: $${toolConfig.revenuePerUse} per execution'));
      console.log(chalk.gray('Estimated daily revenue (100 execs/day): $'), (toolConfig.revenuePerUse * 100).toFixed(2));
      
    } catch (error) {
      console.error(chalk.red('\n❌ Error generating tool:'), error.message);
    }
  }

  console.log('');
  return main();
}

main().catch(console.error);
